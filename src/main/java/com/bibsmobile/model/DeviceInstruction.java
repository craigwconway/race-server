/**
* This holds reports from {@link FuseDevice} objects when uploaded.
*
* @author  galen
* @version 1.0
* @since   2015-12-21 
*/
@Configurable
@Entity
public class DeviceInstruction {
	/**
	 * Autogenerated device ID
	 */
	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	private Long id;

	/**
	 * Associated Device
	 */
	@ManyToOne
	private FuseDevice device;
	
	/**
	 * Associated reader status
	 */
	@Enumerated
	private ReaderAction action;

	@Temporal(TemporalType.TIMESTAMP)
	private Date created;
	
	@Temporal(TemporalType.TIMESTAMP)
	private Date timeStart;
	
	/**
	 * Default constructor for the JSON. (For the JSON).
	 */
	public DeviceInstruction() {}
	
	/**
	 * Construct a device status from a ReaderStatus + FuseDevice
	 * @param readerStatus
	 * @param device
	 */
	public DeviceInstruction(ReaderAction readerAction, FuseDevice device) {
		this.created = new Date();
		this.timeStart = new Date();
		this.device = device;
		this.action = readerAction;
	}
	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the device
	 */
	public FuseDevice getDevice() {
		return device;
	}

	/**
	 * @param device the device to set
	 */
	public void setDevice(FuseDevice device) {
		this.device = device;
	}

	/**
	 * @return the readerStatus
	 */
	public ReaderAction getAction() {
		return action;
	}

	/**
	 * @param readerStatus the readerStatus to set
	 */
	public void setReaderAction(ReaderAction action) {
		this.action = action;
	}

	/**
	 * @return the created
	 */
	public Date getCreated() {
		return created;
	}

	/**
	 * @param created the created to set
	 */
	public void setCreated(Date created) {
		this.created = created;
	}

	public static DeviceInstruction findDeviceStatus(Long id) {
        if (id == null)
            return null;
        return entityManager().find(DeviceInstruction.class, id);
    }
	
	public String toJson() {
		ObjectMapper mapper = new ObjectMapper();
		try {
			return mapper.writeValueAsString(this);
		} catch (JsonProcessingException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
			return null;
		}
		
	}
    //
	// -------------------------------------------
	// -------------------------------------------
	// Database transaction functions
	// -------------------------------------------
	// Right now we only want to find a device info
	// with the starting ID. If they have tampered
	// with the device id, the user is trying to exploit
	// the system
	
    public static long countDeviceStatuses() {
        return entityManager().createQuery("SELECT COUNT(o) FROM DeviceStatus o", Long.class).getSingleResult();
    }
    
    public static DeviceInstruction getLatestStatusForDevice(FuseDevice device) {
    	EntityManager em = entityManager();
    	try {
    		return em.createQuery("SELECT o from DeviceStatus o ORDER BY id DESC", DeviceInstruction.class).setMaxResults(1).setFirstResult(0).getSingleResult();
    	} catch (Exception e) {
    		return null;
    	}
    }
	// -------------------------------------------
	// -------------------------------------------	
	// -------------------------------------------
	// Begin the gramps framework overhead:
	// -------------------------------------------
    @PersistenceContext
    transient EntityManager entityManager;    		
    public static EntityManager entityManager() {
        EntityManager em = new DeviceInstruction().entityManager;
        if (em == null)
            throw new IllegalStateException("Entity manager has not been injected (is the Spring Aspects JAR configured as an AJC/AJDT aspects library?)");
        return em;
    }

    @Transactional
    public void persist() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        this.entityManager.persist(this);
    }
    @Transactional
    public void remove() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        if (this.entityManager.contains(this)) {
            this.entityManager.remove(this);
        } else {
            DeviceInstruction attached = DeviceInstruction.findDeviceStatus(this.id);
            this.entityManager.remove(attached);
        }
    }
    @Transactional
    public void flush() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        this.entityManager.flush();
    }
    @Transactional
    public void clear() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        this.entityManager.clear();
    }
    @Transactional
    public DeviceInstruction merge() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        DeviceInstruction merged = this.entityManager.merge(this);
        this.entityManager.flush();
        return merged;
    }
	// -------------------------------------------
	// End the gramps framework overhead:
	// -------------------------------------------
	// -------------------------------------------
}
