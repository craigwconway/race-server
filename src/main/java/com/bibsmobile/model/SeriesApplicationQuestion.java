/**
 * Copy of a badge. All badges are queried on their trigger type and whether they
 * are enabled when a trigger comes in from a queue. If the badge matches, the a
 * UserBadge is created. A badge may be triggered by either an event or a series.
 * @author galen
 *
 */
@Entity
public class SeriesApplicationQuestion {
	/**
	 * Autogenerated id
	 */
	@Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
	
	private String question;
	
    @PersistenceContext
    transient EntityManager entityManager;
    
    public static EntityManager entityManager() {
        EntityManager em = new EventType().entityManager;
        if (em == null)
            throw new IllegalStateException("Entity manager has not been injected (is the Spring Aspects JAR configured as an AJC/AJDT aspects library?)");
        return em;
    }

    @Transactional
    public void persist() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        this.entityManager.persist(this);
    }

    @Transactional
    public void remove() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        if (this.entityManager.contains(this)) {
            this.entityManager.remove(this);
        } else {
            SeriesApplicationQuestion attached = SeriesApplicationQuestion.findBadge(this.id);
            this.entityManager.remove(attached);
        }
    }

    @Transactional
    public void flush() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        this.entityManager.flush();
    }

    @Transactional
    public void clear() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        this.entityManager.clear();
    }

    @Transactional
    public SeriesApplicationQuestion merge() {
        if (this.entityManager == null)
            this.entityManager = entityManager();
        SeriesApplicationQuestion merged = this.entityManager.merge(this);
        this.entityManager.flush();
        return merged;
    }

    /**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the response
	 */
	public String getQuestion() {
		return question;
	}

	/**
	 * @param response the response to set
	 */
	public void setQuestion(String question) {
		this.question = question;
	}

	public static SeriesApplicationQuestion findBadge(Long id) {
        if (id == null)
            return null;
        return entityManager().find(SeriesApplicationQuestion.class, id);
    }
    
    public static List<SeriesApplicationQuestion> findBadgesByEvent(Event event) {
        return entityManager().createQuery("SELECT o FROM Badge AS o WHERE o.event = :event", SeriesApplicationQuestion.class).setParameter("event", event).getResultList();
    }    
}
